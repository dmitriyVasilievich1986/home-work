"use strict"

//#region Функция сложения двух чисел
function two_numbers_summarize(a, b) {
    /**
     * function summarizes two integer numbers
     * @param {number} a first integer number
     * @param {number} b second integer number
     * @returns {number}
     */
    return a + b;
}

//#endregion

//#region Функция вычитания двух чисел
function two_numbers_substracts(a, b) {
    /**
     * function substracts two integer numbers
     * @param {number} a first integer number
     * @param {number} b second integer number
     * @returns {number}
     */
    return a - b;
}

//#endregion

//#region Функция умножения двух чисел
function two_numbers_multiply(a, b) {
    /**
     * function multiply two integer numbers
     * @param {number} a first integer number
     * @param {number} b second integer number
     * @returns {number}
     */
    return a * b;
}

//#endregion

//#region Функция деления двух чисел
function two_numbers_divides(a, b) {
    /**
     * function divides two integer numbers
     * @param {number} a first integer number
     * @param {number} b second integer number
     * @returns {number}
     */
    return a / b;
}

//#endregion

